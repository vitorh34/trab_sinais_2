% --- Executes on button press in impulso.
function impulso_Callback(hObject, eventdata, handles)
% hObject    handle to impulso (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
M=str2double(get(handles.M,'String'));
m=str2double(get(handles.m,'String'));
b=str2double(get(handles.b,'String'));
I=str2double(get(handles.I,'String'));
g=str2double(get(handles.g,'String'));
l=str2double(get(handles.l,'String'));

q = (M + m) * (m * I + l ^ 2) - (m * l) ^ 2;
s = tf ( 's');
P_pend = (m * l * s / q) / (s ^ 3 + (b * (I + m * l ^ 2)) * s ^ 2 / q - ((M + m) * m * g * l ) * s / q - b * m * g * l / q );
P_cart = (((I+m*l^2)/q)*s^2 - (m*g*l/q))/(s^4 + (b*(I + m*l^2))*s^3/q - ((M + m)*m*g*l)*s^2/q - b*m*g*l*s/q);

Kp =str2double(get(handles.Kp,'String'));
Ki =str2double(get(handles.Ki,'String'));
Kd =str2double(get(handles.Kd,'String'));

C = pid(Kp,Ki,Kd);
T = feedback(P_pend,C);
T2 = feedback(1,P_pend*C)*P_cart;

rb = get(get(handles.ui1,'SelectedObject'),'Tag');
if rb == 'rb1'
   tf1=evalc('T');
   set(handles.tf,'String',tf1); 
   t=0:0.01:10;
   impulse(T,t);
   title('Resposta ao impulso da posição do pêndulo devido ao controle PID');
end    
if rb == 'rb2'
   tf2=evalc('T2');
   set(handles.tf,'String',tf2); 
   t = 0:0.01:5;
   impulse(T2, t);
   title('Resposta ao impulso da posição do pêndulo devido ao controle PID');
end    
    
   
